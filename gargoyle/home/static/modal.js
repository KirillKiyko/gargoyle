//java modal windows actions
document.getElementById("learn_java").onclick = function() {
    document.getElementById("modal-java").style.transform = "translateX(0)";
    document.getElementsByClassName("close-area-java")[0].style.transform = "translateX(0)";
}
//java modal close
document.getElementById("learn_more_java").onclick = function(){
    document.getElementById("modal-java").style.transform = "translateX(100%)";
    document.getElementsByClassName("close-area-java")[0].style.transform = "translateX(100%)";
}

document.getElementsByClassName("close-area-java")[0].onclick = function(){
    document.getElementById("modal-java").style.transform = "translateX(100%)";
    document.getElementsByClassName("close-area-java")[0].style.transform = "translateX(100%)";
}

